public class Maths {
    public static void main (String[]args){

        //Maximum 
        System.out.println("Max= " +Math.max(12,14));

        //Minimum
        System.out.println("Min= "+Math.min(10,8));

        // Absolute                   (it converts negative value into positive value we can input any value string, float ,double)
       System.out.println("Abs= " +Math.abs(-20));

       //Squareroot
        System.out.println("Square= " +Math.sqrt(16));

        //Random numbers                              range of random number lies in 0 to 1 not exact but approx to 1 and equal to 0 
        System.out.println("Random= "+Math.random()*10);

        //Power 
         System.out.println("Power= " +Math.pow(2,3));

    }
    
}
